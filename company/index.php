<h2>About Shore Connection</h2>
<h3>Experienced Web Design, Hosting and Development Professionals</h3>
<p>Shore Connection is a small group of Internet design, networking, marketing and programming professionals who came together in 1997 to offer a complete set of services to our clients.  The need to offer professional and reliable, service oriented Internet skills was greater than ever.  Over the years, the industry has seen its ups and downs and more than a few web development companies have come and gone. Shore Connection continues to prosper.</p>
