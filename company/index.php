<p>A year later, many of our client's began approaching us to offer an online shopping cart solution so that they could market their products online.  After a great deal of trials and tribulations, Shore Connection settled on Miva Merchant and became a Miva Certified Business Partner.  The flexible yet generic shopping cart system provides the perfect balance of features, reliability and customization that we and our clients needed.  </p>
